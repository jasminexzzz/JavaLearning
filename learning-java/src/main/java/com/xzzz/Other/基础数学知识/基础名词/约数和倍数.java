package com.xzzz.Other.基础数学知识.基础名词;

public class 约数和倍数 {
    /**
     *
    一. 因数,或称为约数(不能为负数,必须是自然数)
        @see 自然数

        1. 定义：整数a除以整数b(b≠0) 的商正好是整数而没有余数，我们就说b是a的因数。0不是0的因数.

        2. 例如:
            12 / 6 = 2 , 6是12的约数,2也是12的约数.
            12 / 10 = 1 余 2 ,10不是12的约数.


    二.倍数(不能为负数,必须是自然数)

        1. 定义: 一个整数能够被另一个整数整除，这个整数就是另一整数的倍数.

        2. 例如:
            12 / 6 = 2 , 12是6的倍数,12也是2的倍数.

     */
}
