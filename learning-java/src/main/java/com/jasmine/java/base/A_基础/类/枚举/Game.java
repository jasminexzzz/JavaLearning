package com.jasmine.java.base.A_基础.类.枚举;

public class Game {

    private String name;//游戏名称
    private int type;//游戏类型,通过枚举获取

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

}
